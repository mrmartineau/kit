export function getSoundsByCategory(category: SoundCategory): SoundName[] {
  return sounds.filter((s) => s.startsWith(category)) as SoundName[];
}

/**
 * Returns the subpath import specifier for a sound file.
 * Use with `new URL()` or your bundler's asset handling.
 *
 * @example
 * ```ts
 * const path = getSoundPath("click_001");
 * // => "@mrmartineau/kit/sounds/click_001.mp3"
 * ```
 */
export function getSoundPath(name: SoundName): string {
  return `@mrmartineau/kit/sounds/${name}.mp3`;
}
